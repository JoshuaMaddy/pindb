#!/usr/bin/env node
/**
 * Extract a curated set of Simple Icons (brand SVG path data) for domains
 * seen in artist/shop link lists, and write a Python module consumed
 * server-side by templates/components/display/entity_links.py.
 *
 * Not wired into `npm run build` — this is a one-off/occasional generation
 * step. Re-run manually after editing DOMAIN_ICONS below (e.g. to add a
 * newly-popular domain), then commit the regenerated Python file.
 */
import { writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import * as simpleIcons from "simple-icons";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "../..");
const OUT_FILE = join(
  ROOT,
  "src/pindb/templates/components/display/brand_icons.py"
);

/**
 * domain match (suffix of the link's hostname) -> simple-icons export key.
 * Ordered by how it should be checked isn't relevant (hostname suffix match
 * is exact-enough given the domain list); keep alphabetical by domain.
 */
const DOMAIN_ICONS = {
  "bigcartel.com": "siBigcartel",
  "bsky.app": "siBluesky",
  "deviantart.com": "siDeviantart",
  "etsy.com": "siEtsy",
  "furaffinity.net": "siFuraffinity",
  "instagram.com": "siInstagram",
  "kickstarter.com": "siKickstarter",
  "ko-fi.com": "siKofi",
  "linktr.ee": "siLinktree",
  "myshopify.com": "siShopify",
  "patreon.com": "siPatreon",
  "tiktok.com": "siTiktok",
  "tumblr.com": "siTumblr",
  "twitter.com": "siX",
  "wixsite.com": "siWix",
  "x.com": "siX",
};

function pyStr(s) {
  return `"${s.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
}

async function main() {
  const entries = [];
  for (const [domain, key] of Object.entries(DOMAIN_ICONS)) {
    const icon = simpleIcons[key];
    if (!icon) {
      console.error(`generate-brand-icons: no simple-icons export "${key}" for domain "${domain}"`);
      process.exit(1);
    }
    entries.push({ domain, title: icon.title, path: icon.path });
  }
  entries.sort((a, b) => a.domain.localeCompare(b.domain));

  const body = entries
    .map(
      (e) =>
        `    ${pyStr(e.domain)}: BrandIcon(title=${pyStr(e.title)}, path=${pyStr(e.path)}),`
    )
    .join("\n");

  const out = `"""Brand icon SVG path data for known link domains.

Generated by scripts/simple-icons/generate-brand-icons.mjs from the
\`simple-icons\` npm package (24x24 viewBox) — do not edit by hand.
To add a domain: edit DOMAIN_ICONS in that script and re-run it.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True, slots=True)
class BrandIcon:
    title: str
    path: str


BRAND_ICONS_BY_DOMAIN: dict[str, BrandIcon] = {
${body}
}
`;

  await writeFile(OUT_FILE, out, "utf8");
  console.log(`brand icons: ${entries.length} domains -> ${OUT_FILE}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
